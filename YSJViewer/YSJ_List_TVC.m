- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    static NSString *CellIdentifier = @"YSJ_List_Cell";
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier forIndexPath:indexPath];
    
    // Configure the cell...
    
    // Cell Image Icon
    UIImageView *cellIcon = (UIImageView *)[cell viewWithTag:100];
//    cellIcon.image = [self imageForRating:player.rating];
    
    // 压缩机名字
    UILabel *labName = (UILabel *)[cell viewWithTag:101];
    labName.text = @"邯钢二冷";
    
    // 压缩机编号
    UILabel *labNumber = (UILabel *)[cell viewWithTag:102];
    labNumber.text = @"A9SX1S062-1";
    
    // 压缩机位置？
    UILabel *labPosition = (UILabel *)[cell viewWithTag:103];
    labPosition.text = @"P700";
    
    // 压缩机状态
    UILabel *labStatus = (UILabel *)[cell viewWithTag:104];
    labStatus.text = @"在线";
    
    //
    return cell;
}

